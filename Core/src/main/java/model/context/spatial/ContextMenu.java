package model.context.spatial;

/**
 * Eine Enumeration, welche das Menü festlegt, dass durch die Interaktion mit einem räumlichen Kontext beim Benutzer
 * geöffnet werden soll.
 */
public enum ContextMenu {

    /**
     * Repräsentiert das Menü, dass durch die Interaktion mit dem Portal beim Benutzer geöffnet werden soll.
     */
    PORTAL_MENU,

    /**
     * Repräsentiert das Menü, dass durch die Interaktion mit dem Musikstreamer beim Benutzer geöffnet werden soll.
     */
    MUSIC_STREAMER_MENU,

    /**
     * Repräsentiert das Menü, dass durch die Interaktion mit dem Musikspieler beim Benutzer geöffnet werden soll.
     */
    MUSIC_PLAYER_MENU,

    /**
     * Repräsentiert das Menü, dass durch die Interaktion mit der Rezeption beim Benutzer geöffnet werden soll.
     */
    ROOM_RECEPTION_MENU,

    /**
     * Repräsentiert das Menü, dass durch die Interaktion mit dem Bereichsplaner beim Benutzer geöffnet werden soll.
     */
    AREA_PLANNER_MENU,

    /**
     * Repräsentiert das Menü, dass durch die Interaktion mit dem Spieltisch beim Benutzer geöffnet werden soll.
     */
    GAME_BOARD_MENU,

    /**
     * Repräsentiert das Menü, dass durch die Interaktion mit dem Sitzplatz beim Benutzer geöffnet werden soll.
     */
    SEAT_MENU
}
