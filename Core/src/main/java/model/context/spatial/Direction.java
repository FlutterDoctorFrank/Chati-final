package model.context.spatial;

/**
 * Eine Enumeration, die die Richtung in die ein Benutzer schauen kann, repräsentiert.
 */
public enum Direction {

    /**
     * Repräsentiert die Blickrichtung nach oben.
     */
    UP,

    /**
     * Repräsentiert die Blickrichtung nach rechts.
     */
    RIGHT,

    /**
     * Repräsentiert die Blickrichtung nach unten.
     */
    DOWN,

    /**
     * Repräsentiert die Blickrichtung nach links.
     */
    LEFT
}
